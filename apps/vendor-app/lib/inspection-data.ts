export function getInspectionDataForVehicleType(vehicleType: string): VehicleInspectionData | null {
  return inspectionData[vehicleType] || null
}

export function calculateTotalInspectionCost(
  vehicleType: string,
  inspectionResults: { [pointId: string]: keyof InspectionPoint["conditions"] },
): number {
  const data = getInspectionDataForVehicleType(vehicleType)
  if (!data) return 0

  let totalCost = 0

  Object.entries(data.categories).forEach(([_, category]) => {
    category.inspectionPoints.forEach((point) => {
      const selectedCondition = inspectionResults[point.id]
      if (selectedCondition && point.conditions[selectedCondition]) {
        totalCost += point.conditions[selectedCondition].reduction
      }
    })
  })

  return totalCost
}

export function getInspectionSummary(
  vehicleType: string,
  inspectionResults: { [pointId: string]: keyof InspectionPoint["conditions"] },
): {
  totalPoints: number
  completedPoints: number
  totalCost: number
  categoryBreakdown: { [category: string]: { points: number; cost: number } }
} {
  const data = getInspectionDataForVehicleType(vehicleType)
  if (!data) return { totalPoints: 0, completedPoints: 0, totalCost: 0, categoryBreakdown: {} }

  const categoryBreakdown: { [category: string]: { points: number; cost: number } } = {}
  let completedPoints = 0
  let totalCost = 0

  Object.entries(data.categories).forEach(([categoryKey, category]) => {
    let categoryPoints = 0
    let categoryCost = 0

    category.inspectionPoints.forEach((point) => {
      const selectedCondition = inspectionResults[point.id]
      if (selectedCondition) {
        categoryPoints++
        completedPoints++
        if (point.conditions[selectedCondition]) {
          const reduction = point.conditions[selectedCondition].reduction
          categoryCost += reduction
          totalCost += reduction
        }
      }
    })

    categoryBreakdown[category.name] = {
      points: categoryPoints,
      cost: categoryCost,
    }
  })

  return {
    totalPoints: data.totalPoints,
    completedPoints,
    totalCost,
    categoryBreakdown,
  }
}

