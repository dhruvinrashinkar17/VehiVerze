export interface InspectionPoint {
  id: string
  category: string
  subcategory: string
  question: string
  basePrice: number
  conditions: {
    excellent: { label: string; reduction: number }
    veryGood: { label: string; reduction: number }
    good: { label: string; reduction: number }
    average: { label: string; reduction: number }
    poor: { label: string; reduction: number }
  }
}

export interface VehicleInspectionData {
  vehicleType: string
  totalPoints: number
  categories: {
    [key: string]: {
      name: string
      points: number
      inspectionPoints: InspectionPoint[]
    }
  }
  basePriceRange: {
    min: number
    max: number
  }
}

